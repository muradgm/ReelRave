// pagination
const pagination = (arr, page, limit) => {
  const start = (page - 1) * limit;
  const end = page * limit;
  return arr.slice(start, end);
};

export const getActors = (arr, limit, page) => {
  const actors = pagination(arr, page, limit);
  return actors;
};
